package school.mjc.stage0.module3.task4;

public class IntRepresentation {
    public static void main(String[] args) {
        int first = 99;
        int second = 104;
        int third = 97;
        int fourth = 114;
        char c = (char) first;
        char h = (char) second;
        char a = (char) third;
        char r = (char) fourth;
        System.out.print(c);
        System.out.print(h);
        System.out.print(a);
        System.out.print(r);
    }
}
